function shuffleString(s: string): string {
  const arr = s.split('');
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr.join('');
}

export type WordLengthOption = 4 | 5 | 6 | 'any';

function getWordsByLength(length: WordLengthOption): string[] {
  if (length === 'any') return WORD_SCRAMBLE_WORDS;
  return WORD_SCRAMBLE_WORDS.filter((w) => w.length === length);
}

export function getRandomScrambleWord(length: WordLengthOption = 'any'): string {
  const pool = getWordsByLength(length);
  if (pool.length === 0) return WORD_SCRAMBLE_WORDS[Math.floor(Math.random() * WORD_SCRAMBLE_WORDS.length)];
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getScrambledWord(word: string): string {
  let scrambled = shuffleString(word);
  while (scrambled === word && word.length > 1) {
    scrambled = shuffleString(word);
  }
  return scrambled;
}
